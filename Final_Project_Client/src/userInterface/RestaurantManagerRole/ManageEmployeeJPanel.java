    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String Name = txtName.getText();
        String account = txtUserAccount.getText();
        String password = txtPassword.getText();
        if ((Name.equals("")) || (account.equals("")) || password.equals("")) {
            JOptionPane.showMessageDialog(null, "Invalid information!!");
            return;
        }
        EnterpriseLevelOrganization organization = (EnterpriseLevelOrganization) comboOrganization.getSelectedItem();
        if (organization == null) {
            JOptionPane.showMessageDialog(null, "organization is null! not created!!!");
            return;
        }
        boolean created = system.checkEcosystemUser(account);
        if (!created) {
            Person p = organization.getPersonDirectory().createPerson(Name);
            Role role = (Role) new RestaurantStaffRole();

            organization.getUserAccountDirectory().createUserAccount(account, password, p, role);
            JOptionPane.showMessageDialog(null, "Created Successfully!");
            try {
                streamOut.reset();
                streamOut.writeObject(system);
                streamOut.flush();
            } catch (Exception e) {
                System.out.println("Error:" + e.getMessage());
            }
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "UserAccount Already exist");
        }
    }//GEN-LAST:event_btnAddActionPerformed

