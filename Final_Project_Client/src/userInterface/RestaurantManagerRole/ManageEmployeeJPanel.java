    private void populateCombo() {
        comboOrganization.removeAllItems();
        for (EnterpriseLevelOrganization o : restaurant.getOrganizationDirectory().getOrganizationList()) {
            comboOrganization.addItem(o);
        }
    }

    private void populateTable() {
        EnterpriseLevelOrganization organization = (EnterpriseLevelOrganization) comboOrganization.getSelectedItem();
        if (organization != null) {
            DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
            model.setRowCount(0);
            for (UserAccount u : organization.getUserAccountDirectory().getUserAccountList()) {
                Object[] row = new Object[3];
                row[0] = u.getPerson();
                row[1] = u;
                row[2] = u.getPassword();
                model.addRow(row);
            }
        }
    }

