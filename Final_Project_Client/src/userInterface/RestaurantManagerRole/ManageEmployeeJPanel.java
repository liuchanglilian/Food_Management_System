    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblEmployee.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select");
            return;
        } else {
            UserAccount u = (UserAccount) tblEmployee.getValueAt(selectedRow, 0);
            EnterpriseLevelOrganization organization = (EnterpriseLevelOrganization) comboOrganization.getSelectedItem();
            organization.getUserAccountDirectory().getUserAccountList().remove(u);
            organization.getPersonDirectory().getPersonList().remove(u.getPerson());
            JOptionPane.showMessageDialog(null, "Deleted succeed!!");
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed


