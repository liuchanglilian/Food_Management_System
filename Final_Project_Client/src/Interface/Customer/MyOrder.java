    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select");
            return;
        }
        WorkRequest r = (WorkRequest) jTable1.getValueAt(selectedRow, 0);
        if (r.getDeliveryMan() == null) {
            JOptionPane.showMessageDialog(null, "Hasn't been sent out, be patient please");
            return;
        }
        r.setStatus("Received");
        userAccount.getTheOrderIsentoutQueue().getWorkRequestList().remove(r);
        r.getDeliveryMan().getWorkQueue().getWorkRequestList().remove(r);
        JOptionPane.showMessageDialog(null, "Reveived");
        populateTable();
        try {
            streamOut.reset();
            streamOut.writeObject(system);
            streamOut.flush();
        } catch (Exception exception) {
            System.out.println("Error:" + exception.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

