    public String timeSince(Date requestDate) {

        Date current = new Date();
        long diff = current.getTime() - requestDate.getTime();
        long diffMinutes = diff / (60 * 1000);
        return String.valueOf(diffMinutes);
    }

    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (WorkRequest r : userAccount.getTheOrderIsentoutQueue().getWorkRequestList()) {
            Object row[] = new Object[4];
            row[0] = r;
            row[1] = r.getRequestType();
            row[2] = r.getStatus();
            row[3] = timeSince(r.getRequestDate());
            model.addRow(row);

        }

    }

