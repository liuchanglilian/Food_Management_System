    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int mark = 0;
        if (jRadioButton1.isSelected()) {
            mark = 5;
        }
        if (jRadioButton2.isSelected()) {
            mark = 4;
        }
        if (jRadioButton3.isSelected()) {
            mark = 3;
        }
        if (jRadioButton4.isSelected()) {
            mark = 2;
        }
        if (jRadioButton5.isSelected()) {
            mark = 1;
        }
        if (mark == 0) {
            JOptionPane.showMessageDialog(null, "Please select");
            return;
        }
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select");
            return;
        }
        WorkRequest r = (WorkRequest) jTable1.getValueAt(selectedRow, 0);
        if (!(r instanceof FoodOrderRequest)) {
            JOptionPane.showMessageDialog(null, "You Can only mark on restaurants");
            return;
        }
        FoodOrderRequest a = (FoodOrderRequest) r;
        Restaurant restaurant = (Restaurant) r.getReceiver();
        restaurant.makeMark(mark);
        if (a.isMarked()) {
            JOptionPane.showMessageDialog(null, "Is alreadyMarkedF");
        }
        a.setMarked(true);
        JOptionPane.showMessageDialog(null, "Make mark on the restaurant!");
    }//GEN-LAST:event_jButton2ActionPerformed


